#include <libft.h>

size_t	ft_strncmp(char *s1, char *s2, size_t n)
{
	unsigned int	i;
	int				d;

	i = 0;
	d = 1;
	if (n == 0)
		return (0);
	while (s1[i] != '\0' && s2[i] != '\0' && s1[i] == s2[i] && i < n)
	{
		d = s1[i] - s2[i];
		if (i == (n - 1) && d == 0)
		{
			return (s1[i] - s2[i]);
		}
		i++;
	}
	return (s1[i] - s2[i]);
}
