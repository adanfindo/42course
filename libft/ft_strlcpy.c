#include <libft.h>

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	unsigned int	char_num;
	unsigned int	index;

	char_num = 0;
	index = 0;
	while (src[char_num] != '\0')
		char_num++;
	if (dstsize != 0)
	{
		while (src[index] != '\0' && index < (dstsize - 1))
		{
			dst[index] = src[index];
			index++;
		}
		dst[index] = '\0';
	}
	return (char_num);
}
